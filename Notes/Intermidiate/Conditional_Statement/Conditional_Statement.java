public class Conditional_Statement{
    public static void main(String[] args) {
        for(int x=0; x<5; x++){
            System.out.println(x);
        }
        int i = 0;
        while (i<10){
            System.out.println(i);
            i++;
            
            
        }
        if (i==10){
                System.out.println("Now : "+i);
            }
        else{
            System.out.println(i);
        }
    }
}